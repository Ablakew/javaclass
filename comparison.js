/*comparisons are used to evaluate the relationship between two values and return a boolean value
 (true or false) based on the result.
 */

 //1. Equal to (== or ===):

console.log(5 == 5);    // Output: true
console.log("5" === 5); // Output: false (strict equality, compares both value and type)

//2. Not equal to (!= or !==):

console.log(5 != 10);   // Output: true
console.log("5" !== 5); // Output: true (strict inequality, compares both value and type)

//3. Greater than (>):

console.log(10 > 5);    // Output: true
console.log(5 > 10);    // Output: false

//4. Less than (<):

console.log(5 < 10);    // Output: true
console.log(10 < 5);    // Output: false

//5. Greater than or equal to (>=):

console.log(10 >= 5);   // Output: true
console.log(5 >= 10);   // Output: false

//6. Less than or equal to (<=):

console.log(5 <= 10);   // Output: true
console.log(10 <= 5);   // Output: false